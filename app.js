function renderCategory(categoryName, containerId) {
  const container = document.getElementById(containerId);

  directory[categoryName].forEach(app => {
    const card = document.createElement("div");
    card.className = "tool-card";

    card.innerHTML = `
      <img src="${app.logo}" class="tool-logo">
      <h3>${app.name}</h3>
      <div class="social-links">
        ${app.github ? `<a href="${app.github}" class="btn-pill"><img src="medias/githbub.svg" class="btn-icon"></a>` : ""}
        ${app.fdroid ? `<a href="${app.fdroid}" class="btn-pill"><img src="medias/fdroid.svg" class="btn-icon"></a>` : ""}
        ${app.website ? `<a href="${app.website}" class="btn-pill"><img src="medias/link.svg" class="btn-icon"></a>` : ""}
      </div>
    `;

    container.appendChild(card);
  });
}

